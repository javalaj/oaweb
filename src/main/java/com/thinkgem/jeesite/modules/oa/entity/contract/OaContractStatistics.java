package com.thinkgem.jeesite.modules.oa.entity.contract;

import java.util.Date;


import com.thinkgem.jeesite.common.persistence.DataEntity;

/**
 * 销售合同管理Entity
 * @author 李廷龙
 * @version 2016-12-12
 */
public class OaContractStatistics extends DataEntity<OaContractStatistics> {
	
	
	private static final long serialVersionUID = 1L;
	private String projectNo;		// 项目编号
	private String projectName;		// 项目名称
	private String projectStageLabel;		// 项目阶段
	private Double expectedMoney;		// 预计金额
	private String contractNo;		// 合同编号
	private String contractName;		// 合同名称
	private Double contractMoney;		// 合同金额
	private Double completedMoney;		// 回款金额
	private Double leftMoney;		// 待回款金额
	private Date establishmentTime;		// 立项日期
	private String contractType;		// 服务内容
	private Date beginEstablishmentTime;		// 开始 项目立项日期
	private Date endEstablishmentTime;		// 结束 项目立项日期
	private String realContractType;		// 合同类别
	public OaContractStatistics() {
		super();
	}

	public OaContractStatistics(String id){
		super(id);
	}

	public String getProjectNo() {
		return projectNo;
	}

	public void setProjectNo(String projectNo) {
		this.projectNo = projectNo;
	}

	public String getProjectName() {
		return projectName;
	}

	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}

	public String getProjectStageLabel() {
		return projectStageLabel;
	}

	public void setProjectStageLabel(String projectStageLabel) {
		this.projectStageLabel = projectStageLabel;
	}

	public Double getExpectedMoney() {
		return expectedMoney;
	}

	public void setExpectedMoney(Double expectedMoney) {
		this.expectedMoney = expectedMoney;
	}

	public String getContractNo() {
		return contractNo;
	}

	public void setContractNo(String contractNo) {
		this.contractNo = contractNo;
	}

	public String getContractName() {
		return contractName;
	}

	public void setContractName(String contractName) {
		this.contractName = contractName;
	}

	public Double getContractMoney() {
		return contractMoney;
	}

	public void setContractMoney(Double contractMoney) {
		this.contractMoney = contractMoney;
	}

	public Double getCompletedMoney() {
		return completedMoney;
	}

	public void setCompletedMoney(Double completedMoney) {
		this.completedMoney = completedMoney;
	}

	public Double getLeftMoney() {
		return leftMoney;
	}

	public void setLeftMoney(Double leftMoney) {
		this.leftMoney = leftMoney;
	}

	public Date getEstablishmentTime() {
		return establishmentTime;
	}

	public void setEstablishmentTime(Date establishmentTime) {
		this.establishmentTime = establishmentTime;
	}

	public String getContractType() {
		return contractType;
	}

	public void setContractType(String contractType) {
		this.contractType = contractType;
	}

	public Date getBeginEstablishmentTime() {
		return beginEstablishmentTime;
	}

	public void setBeginEstablishmentTime(Date beginEstablishmentTime) {
		this.beginEstablishmentTime = beginEstablishmentTime;
	}

	public Date getEndEstablishmentTime() {
		return endEstablishmentTime;
	}

	public void setEndEstablishmentTime(Date endEstablishmentTime) {
		this.endEstablishmentTime = endEstablishmentTime;
	}

	public String getRealContractType() {
		return realContractType;
	}

	public void setRealContractType(String realContractType) {
		this.realContractType = realContractType;
	}

		
}